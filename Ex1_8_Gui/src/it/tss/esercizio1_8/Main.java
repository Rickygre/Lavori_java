    public static String analizza(int x) {
        String ris;
        int n = 1;
        int sommaDivisori = 0;

        while (n < x) {
            if (x % n == 0) {
                sommaDivisori += n;

            }
            /*n = n + 1;
            n += 1;    modi differenti di scrivere stesso significato*/
            n++;

        }

        if (sommaDivisori == x) {
            ris = "perfetto";
        } else if (sommaDivisori > x) {
            ris = "abbondante";
        } else {
            ris = "difettivo";
        }

        return ris;

    }

